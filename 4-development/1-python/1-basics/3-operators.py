# arithmetic operators
sum = 10 + 5
sub = 10 - 5
mul = 10 * 5
div = 10 / 5
mod = 10 % 5
exp = 10 ** 5

# assignment operators
assig = 10
assig += 5 # assig = assig + 5 
assig -= 5 # assig = assig - 5
assig *= 5 # assig = assig * 5
assig /= 5 # assig = assig / 5
assig %= 5 # assig = assig % 5
assig **= 5 # assig = assig ** 5

# comparison operators
equal = 10 == 5
not_equal = 10 != 5
greater_than = 10 > 5
less_than = 10 < 5
greater_than_or_equal = 10 >= 5
less_than_or_equal = 10 <= 5

# logical operators
and_op = True and False
or_op = True or False
not_op = not True

# bitwise operators
and_op = True & False # both must be true
or_op = True | False # one or both can be true
xor_op = True ^ False # just one can be true

left_shift_1 = 10 << 1 # 2¹ * 10 -> 20
left_shift_2 = 10 << 3 # 2³ * 10 -> 80
left_shift_3 = 10 << 4 # 2⁴ * 10 -> 160

right_shift_1 = 10 >> 1 # 10 / 2¹ -> 5
right_shift_2 = 10 >> 3 # 10 / 2³ -> 1
right_shift_4 = 10 >> 4 # 10 / 2⁴ -> 0, and all after that is 0

# identity operators
is_op = 10 is 5
is_not_op = 10 is not 5

# membership operators
in_op = 10 in [1, 2, 3, 4, 5]
not_in_op = 10 not in [1, 2, 3, 4, 5]
