# if
if 5 > 2:
    print("Five is greater than two!")
elif 5 == 2:
    print("Five is equal to two!")
else:
    print("Five is less than two!")

# if short-hand
if 5 > 2: print("5 is greater than 2")
if 5 > 2 and 5 < 10: print("Five is greater than two and less than ten!")

# if short-hand with else
print("5 is greater than 2") if 5 > 2 else print("2 is greater than 5")
